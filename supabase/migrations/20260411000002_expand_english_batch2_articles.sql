## Common Pronunciation Mistakes and Fixes

### Mistake 1: Not Aspirating Enough
English speakers are lazy with aspiration. Place your hand in front of your mouth. When you say ㅋ, you should feel a strong puff of air. When you say ㄱ, almost no air.

### Mistake 2: Wrong "R/L" for ㄹ
Korean ㄹ is neither English "R" nor English "L." It's a flap [ɾ] between vowels (like the "tt" in American "butter") and an "L" at the end of syllables. Arabic speakers: think of it as between ر and ل.

### Mistake 3: Adding Stress Where There Is None
English has stress patterns (DA-ddy, comPU-ter). Korean is relatively flat — every syllable gets roughly equal emphasis. Don't stress random syllables.

### Mistake 4: Wrong Vowel for ㅓ (eo)
This is NOT the "o" in "go." It's closer to the "u" in "fun" or "but." Many beginners say 서울 as "so-ul" when it should be closer to "suh-ul."

### Mistake 5: Ignoring Sound Change Rules
Reading 좋아요 as "joh-a-yo" instead of "jo-a-yo" (ㅎ drops before vowels in some contexts). These rules are essential for natural pronunciation.

