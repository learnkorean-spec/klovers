SET content = $$## Why Korean Honorifics Matter More Than Grammar

If you get grammar right but use the wrong formality level, Koreans will perceive you as rude (if too informal), cold and distant (if overly formal), or confused about social boundaries. Conversely, correct honorific usage makes Koreans immediately respect you — even if your grammar has errors.

Understanding honorifics is literally understanding Korean culture through language. In Arabic, you have some formal/informal distinctions (أنتَ vs حضرتك), but Korean takes this concept to an entirely different level.

---

## The Core Concept: 존댓말 vs 반말

Before diving into all 6 levels, understand the two fundamental categories:

- **존댓말 (jondaenmal)** = Respectful/polite speech → Default with everyone
- **반말 (banmal)** = Casual/informal speech → Only with permission

**The golden rule:** Always start with 존댓말. Switch to 반말 only when the other person explicitly suggests it.

---

## The 4 Formality Levels You Actually Need

Korean technically has 6–7 speech levels, but modern Korean uses 4 regularly. Focus on these:

### Level 1: 반말 — Intimate/Casual Speech

**Usage**: Between close friends of the same age, romantic partners, adults speaking to young children, or people who explicitly agreed to speak casually.

**Verb endings**: -아/-어, -지, -네, -군

**Examples:**
- "오늘 뭐 했어?" (What did you do today?)
- "너 한국어 잘하네!" (You're good at Korean!)
- "밥 먹었어?" (Have you eaten?)

**Critical rule**: You don't use 반말 unless the other person initiated it. It's a privilege, not a default. Using 반말 with someone who didn't agree is considered rude — like calling your boss by their first name on your first day.

**How to transition**: Wait for them to say "반말 해도 돼?" (Can we speak casually?) or "우리 반말하자" (Let's use casual speech). Accept explicitly: "네, 좋아요!" (Yes, sounds good!).

### Level 2: 해요체 — Polite Informal (THE DEFAULT)

**Usage**: The safest, most versatile level. Use with strangers, acquaintances, colleagues, service workers — basically everyone in daily life.

**Verb endings**: -아요/-어요

**Examples:**
- "안녕하세요? 어디 가세요?" (Hi, where are you going?)
- "커피 마실래요?" (Want to drink coffee?)
- "오늘 날씨 좋아요." (The weather is nice today.)

**Rule of thumb**: If you only learn one speech level, learn this one. It's appropriate for 80% of situations.

### Level 3: 합쇼체 — Polite Formal

**Usage**: News broadcasts, business presentations, official speeches, talking to customers, military commands.

**Verb endings**: -습니다/-ㅂ니다 (statements), -습니까/-ㅂ니까 (questions)

**Examples:**
- "반갑습니다." (Nice to meet you.)
- "감사합니다." (Thank you.)
- "회의를 시작하겠습니다." (We will begin the meeting.)

**When to use**: Job interviews, presentations, speaking to large groups, or when you want to sound very professional.

### Level 4: 높임말 — Honorific/Elevated Speech

**Usage**: Speaking about or to elders, superiors, and people you deeply respect. Uses special honorific vocabulary and verb forms.

**Key changes:**

| Regular | Honorific | Meaning |
|---------|----------|---------|
| 먹다 (meokda) | 드시다 (deusida) | to eat |
| 자다 (jada) | 주무시다 (jumusida) | to sleep |
| 있다 (itda) | 계시다 (gyesida) | to be/exist |
| 말하다 (malhada) | 말씀하시다 (malsseum-hasida) | to speak |
| 집 (jip) | 댁 (daek) | home |
| 이름 (ireum) | 성함 (seongham) | name |
| 나이 (nai) | 연세 (yeonse) | age |
| 밥 (bap) | 진지 (jinji) | meal |

**Examples:**
- "할머니, 진지 드셨어요?" (Grandmother, have you eaten?) — Using 진지 and 드시다 instead of 밥 and 먹다
- "선생님께서 말씀하셨습니다." (The teacher spoke.) — Using 께서 instead of 이/가 and 말씀하다 instead of 말하다

