## Step-by-Step Practice Routine (15 Minutes Daily)

### Week 1: Master the Three-Way Distinction

**Daily routine (15 min):**
1. **Minimal pairs** (5 min): Alternate between 가/카/까, 다/타/따, 바/파/빠. Record yourself.
2. **Tissue test** (3 min): Use the tissue to verify aspiration differences.
3. **Listening drill** (7 min): Use YouTube or Forvo to hear native speakers say these pairs. Can you identify which is which?

### Week 2: Master Vowels

**Daily routine (15 min):**
1. **Vowel pairs** (5 min): Contrast ㅓ vs ㅗ, ㅡ vs ㅜ, ㅏ vs ㅓ. These are the most confused pairs.
2. **Word practice** (5 min): Say 10 words containing tricky vowels. Record and compare.
3. **Shadowing** (5 min): Pick a 30-second K-drama clip. Repeat exactly what you hear.

### Week 3: Sound Change Rules

**Daily routine (15 min):**
1. **Linking practice** (5 min): Read 먹어요, 한국어, 있어요 out loud. Practice the linking.
2. **Nasalization** (5 min): Practice 합니다, 감사합니다, 한국말 with correct nasal sounds.
3. **Full sentences** (5 min): Read 3 Korean sentences slowly, applying all rules. Speed up gradually.

### Week 4: Putting It All Together

**Daily routine (15 min):**
1. **Shadowing** (10 min): Shadow a 1-minute K-drama or news clip. Match speed and intonation.
2. **Self-assessment** (5 min): Record yourself reading a Korean paragraph. Compare to native audio. Note 3 things to improve.

