## The Korean Sound System: Consonants

Korean has 14 basic consonants and 5 double (tense) consonants. The most important concept for non-native speakers is the **three-way distinction**: plain, aspirated, and tense.

### The Three-Way Stop System

English has a two-way system (voiced vs. voiceless): "b" vs "p". Korean has a **three-way system**:

| Type | ㄱ series | ㄷ series | ㅂ series | ㅈ series |
|------|----------|----------|----------|----------|
| Plain (lax) | ㄱ (g) | ㄷ (d) | ㅂ (b) | ㅈ (j) |
| Aspirated | ㅋ (kh) | ㅌ (th) | ㅍ (ph) | ㅊ (ch) |
| Tense (fortis) | ㄲ (kk) | ㄸ (tt) | ㅃ (pp) | ㅉ (jj) |

**Plain consonants** (ㄱ, ㄷ, ㅂ, ㅈ): Soft, little air. Between voiced and voiceless.
**Aspirated consonants** (ㅋ, ㅌ, ㅍ, ㅊ): Strong puff of air. Hold your hand in front of your mouth — you should feel wind.
**Tense consonants** (ㄲ, ㄸ, ㅃ, ㅉ): No air, but muscles in your throat tighten. Short, sharp, and glottalized.

### The Tissue Paper Test

Hold a tissue in front of your mouth:
- **ㄱ (g):** Tissue barely moves
- **ㅋ (kh):** Tissue blows noticeably
- **ㄲ (kk):** Tissue doesn't move at all

This is the single most important pronunciation distinction in Korean. Get this right and Koreans will immediately notice your good pronunciation.

### Arabic Sounds That Help with Korean

Arabic speakers have an advantage with certain Korean sounds:

| Korean Sound | Arabic Parallel | Explanation |
|-------------|----------------|-------------|
| ㅎ (h) | ه (haa) | Almost identical — light "h" from the throat |
| ㅅ (s) | س (siin) | Very similar — sharp "s" sound |
| ㄴ (n) | ن (nuun) | Identical |
| ㅁ (m) | م (miim) | Identical |
| ㅈ (j) | ج (jiim, Egyptian) | Very close to Egyptian Arabic "g/j" |
| ㄲ, ㄸ, ㅃ (tense) | ق, ط, ص (emphatic) | The concept of tense/emphatic consonants exists in both languages |

The tense consonant parallel is significant. Arabic speakers who understand emphatic consonants (ط vs ت, ص vs س) can apply the same concept to Korean tense consonants. This is a real advantage over English speakers.

