## Pronunciation Rules: Sound Changes

Korean spelling is consistent, but pronunciation has important rules where sounds change:

### Rule 1: Final Consonant Neutralization (받침 rules)

At the end of a syllable, many consonants simplify:

| Written | Pronounced As |
|---------|--------------|
| ㄱ, ㅋ, ㄲ | [k] |
| ㄷ, ㅌ, ㅅ, ㅆ, ㅈ, ㅊ, ㅎ | [t] |
| ㅂ, ㅍ | [p] |
| ㄴ | [n] |
| ㅁ | [m] |
| ㅇ | [ŋ] |
| ㄹ | [l] |

Example: 옷 (clothes) is written with ㅅ but pronounced [ot], not [os].

### Rule 2: Linking (연음)

When a syllable ending in a consonant is followed by a syllable starting with ㅇ, the consonant "links" to the next syllable:

- 한국어 → 한구거 [han-gu-geo] (not han-guk-eo)
- 먹어요 → 머거요 [meo-geo-yo]
- 읽어요 → 일거요 [il-geo-yo]

### Rule 3: Nasalization

When certain consonants meet nasals (ㄴ, ㅁ), they become nasal:

- ㄱ + ㄴ → ㅇ + ㄴ: 한국말 → 한궁말 [han-gung-mal]
- ㅂ + ㄴ → ㅁ + ㄴ: 합니다 → 함니다 [ham-ni-da]

### Rule 4: ㅎ Aspiration

When ㅎ meets a plain consonant, it aspirates it:
- ㄱ + ㅎ → ㅋ: 축하 → 추카 [chu-ka]
- ㄷ + ㅎ → ㅌ: 좋다 → 조타 [jo-ta]

