## The Korean Sound System: Vowels

### The 10 Basic Vowels

| Korean | IPA | Sound Description | Example Word |
|--------|-----|-------------------|-------------|
| ㅏ (a) | [a] | Open, like "father" | 아버지 (father) |
| ㅑ (ya) | [ja] | "ya" as in "yacht" | 얘기 (story) |
| ㅓ (eo) | [ʌ] | Like "fun" — deeper than ㅏ | 어디 (where) |
| ㅕ (yeo) | [jʌ] | "yuh" sound | 여행 (travel) |
| ㅗ (o) | [o] | Like "go" — lips rounded | 오늘 (today) |
| ㅛ (yo) | [jo] | "yo" as in "yo-yo" | 요리 (cooking) |
| ㅜ (u) | [u] | Like "moon" — lips very rounded | 우산 (umbrella) |
| ㅠ (yu) | [ju] | Like "you" | 유명 (famous) |
| ㅡ (eu) | [ɯ] | No English equivalent — say "ee" with lips flat and unrounded | 은행 (bank) |
| ㅣ (i) | [i] | Like "see" | 이름 (name) |

### The Hardest Vowel: ㅡ (eu)

This sound doesn't exist in English or Arabic. Here's how to produce it:
1. Say "ee" (이)
2. Keep your tongue in the same position
3. Spread your lips flat (don't round them)
4. The sound should be a flat, neutral "uh" from the back of your mouth

Practice words: 그 (that), 음악 (music), 슬프다 (sad), 크다 (big)

### Modern Vowel Mergers

In modern Seoul Korean, several vowel pairs have merged:
- **ㅐ (ae) and ㅔ (e)** sound identical to most young Koreans — both are [e] like "bed"
- **ㅚ (oe) and ㅙ (wae) and ㅞ (we)** are all pronounced [we] by most speakers

Don't stress about these merged pairs — even native Koreans can't distinguish them by sound. Context tells you which word is meant.

