## Common Honorific Mistakes and How to Fix Them

### Mistake 1: Using 반말 With Someone Who Didn't Agree
**Scenario**: You meet a Korean your age at a café. After chatting for 5 minutes, you switch to 반말.
**Why it's wrong**: They never explicitly agreed. You'll come across as presumptuous or rude.
**Fix**: Start with 해요체. If they switch to 반말 naturally, ask: "반말 해도 될까요?" (Is it okay to speak casually?)

### Mistake 2: Using Honorific Verbs for Yourself or Peers
**Wrong**: "제가 진지를 드셨어요." (I ate a meal — using honorific for yourself)
**Right**: "제가 밥을 먹었어요." (I ate a meal — normal verb for yourself)
**Rule**: Honorific vocabulary is used for people ABOVE you, never for yourself or equals.

### Mistake 3: Mixing Levels in the Same Sentence
**Wrong**: "선생님, 뭐해?" (Teacher, what are you doing? — formal address + casual verb)
**Right**: "선생님, 뭐 하세요?" (Teacher, what are you doing? — consistent formality)

### Mistake 4: Being Too Formal With Close Friends
**Wrong**: Using -습니다 with a Korean friend you've known for 2 years.
**Why it's wrong**: It creates emotional distance. They'll think you don't consider them close.
**Fix**: After sufficient friendship time, when they suggest 반말, accept it.

### Mistake 5: Not Using Honorifics With Elders
**Wrong**: "할아버지, 뭐 해?" (Grandpa, what are you doing? — too casual)
**Right**: "할아버지, 뭐 하세요?" or "할아버지께서 뭐 하고 계세요?"

