## How Syllable Blocks Work

Every Korean syllable block follows one of these patterns:

**Pattern 1: Consonant + Vowel (CV)**
- ㅂ + ㅏ = 바 (ba)
- ㅎ + ㅏ = 하 (ha)

**Pattern 2: Consonant + Vowel + Final Consonant (CVC)**
- ㅂ + ㅏ + ㅂ = 밥 (bap = rice)
- ㅎ + ㅏ + ㄴ = 한 (han = Korea)

**Pattern 3: Consonant + Compound Vowel (CC)**
- ㅎ + ㅘ = 화 (hwa)
- ㄱ + ㅘ = 과 (gwa)

### Block Layout Rules

When the vowel has a **vertical stroke** (ㅏ, ㅓ, ㅣ, etc.), the consonant goes **left** and the vowel goes **right**:
- 가 = ㄱ (left) + ㅏ (right)

When the vowel has a **horizontal stroke** (ㅗ, ㅜ, ㅡ), the consonant goes **on top** and the vowel goes **below**:
- 고 = ㄱ (top) + ㅗ (bottom)

If there's a final consonant (받침), it always goes at the **bottom** of the block:
- 곤 = ㄱ (top) + ㅗ (middle) + ㄴ (bottom)

---

## Writing Stroke Order

Just like Arabic has specific stroke directions, Hangul has rules:

1. **Write left to right** within a block
2. **Write top to bottom** within a block
3. **Horizontal strokes before vertical strokes**
4. **Outside before inside** for enclosed shapes

Practice tip: Use squared paper (grid paper) to keep your blocks uniform. Each syllable block should fit in one square.

