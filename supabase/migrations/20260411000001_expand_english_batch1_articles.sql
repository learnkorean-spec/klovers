SET content = $$## What is Hangul?

Hangul (한글) is the Korean writing system, created in 1443 by King Sejong the Great. Unlike Chinese characters or Japanese kanji, Hangul is a true alphabet — each symbol represents a specific sound. That's why most learners can read it within **24 hours**, making it one of the most logical writing systems ever designed.

King Sejong created Hangul because the common people of Korea couldn't read Classical Chinese characters. He wanted a writing system that anyone could learn quickly. His mission succeeded: Hangul is considered one of the most scientific alphabets in history, and UNESCO even named a literacy prize after King Sejong.

For Arabic speakers, this is great news. You already learned one non-Latin script (Arabic), so picking up a second one is much easier the second time around.

---

## Hangul vs. Arabic Script: Key Differences

If you already read Arabic, here's how Hangul compares:

| Feature | Arabic | Hangul |
|---------|--------|--------|
| Direction | Right to left | Left to right |
| Letters connect? | Yes (cursive) | No (blocks) |
| Vowels written? | Often omitted | Always written |
| Number of letters | 28 | 40 |
| Letter shapes change by position? | Yes (initial/medial/final) | No (consistent shapes) |

The biggest advantage for Arabic speakers: **Hangul always writes vowels explicitly**. Unlike Arabic where you often guess vowels from context, Korean spells everything out. No ambiguity.

---

## The Structure: Letters Build Blocks

Hangul has **40 letters** total:
- **21 vowels** (모음)
- **19 consonants** (자음)

But here's the key insight: letters are grouped into **syllable blocks**, not written left to right in a line like English.

Example: 한 = ㅎ (h) + ㅏ (a) + ㄴ (n) = "han"

Each block represents one syllable. This makes Korean text incredibly compact and easy to segment once you understand the system.

