## Common Mistakes Beginners Make

### Mistake 1: Confusing ㅓ (eo) and ㅗ (o)
These two vowels sound similar to non-native ears but are completely different sounds. Practice: 거 (geo) vs 고 (go).

### Mistake 2: Forgetting that ㅇ is silent at the start
When ㅇ appears at the beginning of a syllable block, it's just a placeholder (Korean requires a consonant slot). At the end of a block, it makes the "ng" sound.

### Mistake 3: Writing letters in a line instead of blocks
Korean is NOT written like English letters in a row. Always group into syllable blocks.

### Mistake 4: Using romanization as a crutch
Stop reading romanized Korean as soon as possible. It creates bad pronunciation habits that are hard to fix later.

