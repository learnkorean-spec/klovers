## Why Korean is Challenging for Arabic Speakers

Arabic and Korean have almost nothing in common linguistically. Here's what makes it hard:

### 1. Different Alphabet
Hangul has 40 letters, Arabic has 28. The good news: Hangul is much simpler because every letter has exactly one sound, and vowels are always written.

### 2. Different Word Order
Korean uses **Subject-Object-Verb (SOV)** order. Arabic uses **Verb-Subject-Object (VSO)** or **Subject-Verb-Object (SVO)**.

| Language | Example |
|----------|---------|
| Arabic | أنا آكل التفاح (I eat the-apple) |
| Korean | 나는 사과를 먹어요 (I apple eat) |
| English | I eat an apple |

### 3. Particles Instead of Prepositions
Korean uses particles (은/는, 이/가, 을/를) attached to nouns to show their role in the sentence. Arabic uses case endings and prepositions. This is one of the hardest concepts for Arabic speakers.

### 4. Honorific System
Korean has multiple speech levels based on who you're talking to. The verb ending changes completely depending on formality. Arabic has some formal/informal distinctions, but Korean takes it much further.

### 5. No Shared Vocabulary
Unlike learning French (which shares vocabulary with Arabic through historical contact), Korean and Arabic have almost zero cognates. Every word must be learned from scratch.

