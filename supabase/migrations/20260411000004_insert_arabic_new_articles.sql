## أخطاء شائعة وإزاي تصلحها

### خطأ 1: ما بتنفخش كفاية
حط إيدك قدام بقك. لما تقول ㅋ، لازم تحس بنفخة هوا قوية. لما تقول ㄱ، تقريباً مفيش هوا.

### خطأ 2: ㄹ الغلط
الكوري ㄹ مش الراء العربية ر ومش اللام ل. هو بين الاتنين — لسانك بينقر سقف الحلق بسرعة. فكر فيه كصوت بين ر و ل.

### خطأ 3: إضافة تشديد مش موجود
الإنجليزية عندها أنماط تشديد (DA-ddy). الكورية تقريباً مسطحة — كل مقطع بياخد نفس القوة تقريباً.

### خطأ 4: نطق ㅓ (eo) غلط
ده **مش** صوت "o" في "go." هو أقرب لصوت "u" في "fun." كتير من المبتدئين بيقولوا 서울 (سيول) كـ "so-ul" والصح أقرب لـ "suh-ul."

