## ليه الكورية صعبة على المتحدثين بالعربية؟

العربية والكورية مفيش بينهم شبه تقريباً:

### 1. أبجدية مختلفة
الهانغول 40 حرف، العربية 28. الخبر الكويس: الهانغول أبسط لأن كل حرف ليه صوت واحد بس، والحركات دايماً مكتوبة.

### 2. ترتيب الكلمات معكوس

| اللغة | المثال |
|-------|--------|
| العربية | أنا **آكل** التفاحة |
| الكورية | 나는 사과를 **먹어요** (أنا التفاحة آكل) |

### 3. الجسيمات بدل حروف الجر
الكورية بتستخدم جسيمات (은/는، 이/가، 을/를) بعد الأسماء. العربية بتستخدم حروف الجر قبلها.

### 4. نظام الاحترام
الكورية عندها مستويات كلام متعددة حسب مين بتكلمه. الفعل نفسه بيتغير شكله كلياً. العربية عندها فرق بسيط (أنتَ/حضرتك)، لكن الكورية بتاخده لمستوى تاني.

### 5. مفيش مفردات مشتركة
على عكس تعلم الفرنسية (اللي فيها كلمات مشتركة مع العربية من التاريخ)، الكورية والعربية مفيش بينهم أي كلمات مشتركة تقريباً.

