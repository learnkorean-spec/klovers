## المفهوم الأساسي: 존댓말 vs 반말

- **존댓말 (jondaenmal)** = كلام مؤدب/احترامي → الافتراضي مع كل الناس
- **반말 (banmal)** = كلام غير رسمي → بس بإذن

**القاعدة الذهبية**: ابدأ دايماً بـ 존댓말. اتحول لـ 반말 بس لما الشخص التاني يقترح ده.

