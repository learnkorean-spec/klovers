## 5. اتعلم أنماط القواعد، مش القواعد النظرية

بدل ما تحفظ قواعد مجردة، اتعلم **أنماط** تقدر تحط فيها كلمات مختلفة:

**نمط: ~고 싶어요 (عايز أعمل ...)**
- 먹고 싶어요 = عايز آكل
- 가고 싶어요 = عايز أروح
- 자고 싶어요 = عايز أنام

اتعلم 2–3 أنماط في الأسبوع وتنوع جملك هينفجر. بعد 3 شهور هيكون عندك 25+ نمط — كفاية للمحادثة الأساسية.

