## الحروف المتحركة الصعبة

### ㅡ (eu) — أصعب حرف متحرك

الصوت ده مش موجود في العربية أو الإنجليزية:
1. قول "إي" (이)
2. خلي لسانك في نفس المكان
3. سطّح شفايفك (ما تديرهمش)
4. الصوت لازم يطلع من خلف البق

كلمات للتمرين: 그 (ده/ذلك)، 음악 (موسيقى)، 크다 (كبير)

### ㅓ (eo) vs ㅗ (o)

الاتنين بيبانوا متشابهين للأذن غير الكورية لكنهم مختلفين تماماً:
- **ㅓ (eo)**: مثل "أُم" — فتحة عميقة من الخلف
- **ㅗ (o)**: مثل "نور" — شفايف مدورة

تمرين: 거 (geo) vs 고 (go). كررها 20 مرة.

