## الأربع مستويات اللي فعلاً محتاجهم

### المستوى 1: 반말 — الكلام غير الرسمي

**الاستخدام**: بين أصدقاء مقربين بنفس العمر، شريك رومانسي، كبار بيكلموا أطفال صغيرين.

**نهايات الأفعال**: -아/-어

**أمثلة**:
- "오늘 뭐 했어?" (عملت إيه النهارده؟)
- "밥 먹었어?" (أكلت؟)

**قاعدة مهمة**: ما تستخدمش 반말 إلا لو الشخص التاني بدأ. ده امتياز مش افتراضي. استخدام 반말 مع حد ما وافقش يُعتبر وقاحة.

**إزاي تنتقل**: استنى يقول "반말 해도 돼?" (ممكن نتكلم غير رسمي؟) أو "우리 반말하자" (يلا نتكلم غير رسمي).

### المستوى 2: 해요체 — المؤدب (الافتراضي)

**الاستخدام**: الأكثر أماناً وتنوعاً. مع الغرباء والمعارف والزملاء — يعني كل الناس في الحياة اليومية.

**نهايات الأفعال**: -아요/-어요

**أمثلة**:
- "안녕하세요? 어디 가세요?" (مرحبا، رايح فين؟)
- "오늘 날씨 좋아요." (الجو كويس النهارده)

**لو هتتعلم مستوى واحد بس**: ده هو. مناسب لـ 80% من المواقف.

### المستوى 3: 합쇼체 — الرسمي

**الاستخدام**: نشرات الأخبار، عروض العمل، الخطب الرسمية.

**نهايات الأفعال**: -습니다/-ㅂ니다

**أمثلة**:
- "반갑습니다." (تشرفنا)
- "감사합니다." (شكراً)
- "회의를 시작하겠습니다." (هنبدأ الاجتماع)

### المستوى 4: 높임말 — الاحترامي

**الاستخدام**: الكلام عن أو مع الكبار في السن والرؤساء. بيستخدم مفردات وأشكال أفعال خاصة.

