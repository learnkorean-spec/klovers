## نظام التمييز الثلاثي

الإنجليزية عندها نظام ثنائي (مجهور vs مهموس): "b" vs "p". الكورية عندها نظام **ثلاثي**: عادي، منفوخ (مهفوف)، ومشدد.

| النوع | سلسلة ㄱ | سلسلة ㄷ | سلسلة ㅂ |
|-------|---------|---------|---------|
| عادي | ㄱ (g) | ㄷ (d) | ㅂ (b) |
| منفوخ | ㅋ (kh) | ㅌ (th) | ㅍ (ph) |
| مشدد | ㄲ (kk) | ㄸ (tt) | ㅃ (pp) |

**العادي** (ㄱ، ㄷ، ㅂ): خفيف، هوا قليل.
**المنفوخ** (ㅋ، ㅌ، ㅍ): نفخة هوا قوية. حط إيدك قدام بقك — لازم تحس بالهوا.
**المشدد** (ㄲ، ㄸ، ㅃ): مفيش هوا، لكن عضلات الحلق بتتشد. قصير وحاد.

### اختبار المنديل

حط منديل ورق قدام بقك:
- **ㄱ (g)**: المنديل بالكاد بيتحرك
- **ㅋ (kh)**: المنديل بيتحرك بوضوح
- **ㄲ (kk)**: المنديل مش بيتحرك خالص

ده أهم تمييز في النطق الكوري. لو ظبطته، الكوريين هيلاحظوا نطقك الكويس فوراً.

