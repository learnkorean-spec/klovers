## إزاي فعلاً تحفظ الكلمات دي

1. **ما تحفظش كلمات لوحدها** — اتعلمها في جمل. "밥" لوحدها سهل تنسيها. "밥 먹었어요?" عبارة حقيقية هتسمعها كل يوم.
2. **اكتب جمل** مش بس الكلمة
3. **استخدم التكرار المتباعد** — تطبيق Anki بيوريك الكلمات قبل ما تنسيها بالظبط
4. **قولها بصوت عالي** — سماع نفسك بيثبت الذاكرة
5. **دوّر عليها في السياق** — شاهد محتوى كوري ولاحظ الكلمات دي
6. **الصق ملاحظات** — حط ورق لاصق على الحاجات في بيتك بأسمائها الكورية

