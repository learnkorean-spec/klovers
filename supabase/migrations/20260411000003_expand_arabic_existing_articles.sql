## المواجهات والدراما

**19. 왜 그랬어요? (wae geuraesseoyo?)** — *ليه عملت كده؟*
نبرة اتهامية، دموع اختيارية. العمود الفقري لكل صراع في الدراما الكورية.

**20. 몰랐어요 (mollasseoyo)** — *مكنتش أعرف*
العذر الكلاسيكي. غالباً بتتقال بعدها 왜 그랬어요 من الطرف التاني.

**21. 믿을 수 없어 (mideul su eopseo)** — *مش مصدق!*
خيانة مؤكدة. بتتقال والنظر في الفراغ.

**22. 거짓말이야 (geojitmariyo)** — *ده كدب!*
في الدراما: لما الحقيقة تطلع. غالباً بتكون متبوعة بعياط.

**23. 다 끝났어 (da kkeutnatsseo)** — *خلاص كله انتهى*
النهاية الدرامية لعلاقة أو مهمة. عادة بتتقال بصوت هادي، وده بيخليها أصعب.

**24. 내 말 들어 (nae mal deureo)** — *اسمعني!*
بتتقال وهو ماسك ذراع التاني. ذروة الفيزيكال في الدراما الكورية.

