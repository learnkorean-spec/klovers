## ترتيب الجملة: SOV

أكبر فرق بين الكورية والعربية هو ترتيب الكلمات:

| اللغة | الترتيب | مثال |
|-------|---------|------|
| العربية (فصحى) | فعل + فاعل + مفعول (VSO) | أكلَ أحمد التفاحة |
| العربية (عامية) | فاعل + فعل + مفعول (SVO) | أحمد أكل التفاحة |
| الكورية | فاعل + مفعول + فعل (SOV) | 아흐메드가 사과를 먹었어요 (أحمد التفاحة أكل) |

**القاعدة الأهم**: الفعل دائماً في نهاية الجملة الكورية. دي القاعدة الوحيدة اللي مش بتتكسر أبداً.

