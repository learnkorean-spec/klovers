## جدول الرسمي vs غير الرسمي

| العبارة | غير رسمي (반말) | مؤدب (존댓말) |
|---------|---------------|--------------|
| مرحبا | 안녕 | 안녕하세요 |
| شكراً | 고마워 | 감사합니다 |
| آسف | 미안해 | 죄송합니다 |
| بحبك | 사랑해 | 사랑해요 |
| إيه؟ | 뭐? | 뭐라고요? |

> **قاعدة**: لو مش متأكد أي مستوى تستخدم، استخدم المؤدب (العمود اليمين). مش هيبقى غلط أبداً إنك تكون مؤدب زيادة في الكورية.

