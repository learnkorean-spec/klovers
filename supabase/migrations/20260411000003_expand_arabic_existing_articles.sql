## الجسيمات (조사): مفتاح القواعد الكورية

الجسيمات هي كلمات صغيرة بتتحط بعد الأسماء لتحديد دورها في الجملة. فكّر فيها كحروف الجر في العربية، لكنها بتيجي **بعد** الكلمة مش قبلها.

### الجسيمات الأساسية

| الجسيم | الوظيفة | مثال كوري | المعنى |
|--------|---------|----------|--------|
| 은/는 | موضوع الجملة (topic) | 저**는** 학생이에요 | أنا طالب |
| 이/가 | فاعل (subject) | 비**가** 와요 | المطر بييجي |
| 을/를 | مفعول به (object) | 밥**을** 먹어요 | بآكل أرز |
| 에 | اتجاه/زمان | 학교**에** 가요 | بروح المدرسة |
| 에서 | مكان حدوث الفعل | 집**에서** 공부해요 | بدرس في البيت |
| 도 | "كمان/أيضاً" | 저**도** 가요 | أنا كمان رايح |
| 하고/와/과 | "مع/و" | 친구**하고** 갔어요 | رحت مع صاحبي |

### متى تستخدم 은/는 vs 이/가؟

ده السؤال الأصعب في القواعد الكورية. القاعدة المبسطة:

- **은/는** (topic): المعلومة المعروفة أو الموضوع العام
  - 저**는** 이집트 사람이에요 (أنا — كموضوع — مصري)
- **이/가** (subject): المعلومة الجديدة أو التأكيد
  - 비**가** 와요 (المطر — ده اللي — بييجي)

> **نصيحة**: في البداية، استخدم 은/는 في معظم المواقف. مع الوقت هتحس الفرق طبيعي.

### مقارنة عربية

| العربية | الكورية | التشابه |
|---------|---------|---------|
| أنا **في** البيت | 집**에** 있어요 | حرف الجر = 에 |
| أكلت **مع** صاحبي | 친구**하고** 먹었어요 | مع = 하고 |
| رحت **ل**لمدرسة | 학교**에** 갔어요 | لـ = 에 |
| بدرس **في** المكتبة | 도서관**에서** 공부해요 | في = 에서 |

الفرق: في العربية حروف الجر بتيجي **قبل** الاسم. في الكورية الجسيمات بتيجي **بعد** الاسم.

