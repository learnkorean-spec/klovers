## الخطوة الأولى: تعلم حروف الهانغول (한글)

الهانغول هو نظام الكتابة الكوري، اخترعه الملك سيجونغ سنة 1443. على عكس الحروف الصينية أو اليابانية، الهانغول أبجدية حقيقية — كل رمز يمثل صوت واحد. لذلك معظم المتعلمين يقدروا يقرأوه في **يوم واحد فقط**.

### مقارنة بين الهانغول والعربية

| الميزة | العربية | الهانغول |
|--------|---------|----------|
| الاتجاه | من اليمين لليسار | من اليسار لليمين |
| الحروف متصلة؟ | نعم (خط متصل) | لا (مقاطع منفصلة) |
| الحركات مكتوبة؟ | غالباً محذوفة | دائماً مكتوبة |
