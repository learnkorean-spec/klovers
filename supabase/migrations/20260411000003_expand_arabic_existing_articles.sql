لو أنت متحدث عربي، القواعد الكورية هتبان غريبة في البداية. ترتيب الجملة معكوس، الأفعال في الآخر، ومفيش تذكير وتأنيث. لكن الخبر الكويس: القواعد الكورية **منطقية ومنتظمة** — مفيش استثناءات كتير زي العربية أو الإنجليزية.

