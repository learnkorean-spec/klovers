| 읽**을 수 있어요** | أقدر أقرأ |

### النمط 3: ~아/어서 (عشان / لأن ...)

| المثال | المعنى |
|--------|--------|
| 배고**파서** 밥 먹었어요 | لأني كنت جعان أكلت |
| 비**가 와서** 집에 있어요 | لأن المطر بينزل، قاعد في البيت |

### النمط 4: ~ㄹ/을 거예요 (هـ ... / ناوي ...)

| المثال | المعنى |
|--------|--------|
| 내일 공부**할 거예요** | بكرة هدرس |
| 한국에 **갈 거예요** | هروح كوريا |

### النمط 5: ~고 있어요 (بـ ... دلوقتي)

| المثال | المعنى |
|--------|--------|
| 공부**하고 있어요** | بدرس دلوقتي |
| 먹**고 있어요** | بآكل دلوقتي |

