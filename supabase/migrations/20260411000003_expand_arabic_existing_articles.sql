## الصفات في الكورية

الصفات الكورية بتتصرف زي الأفعال — عندها نهايات بتتغير:

| الصفة | الشكل القاموسي | الشكل المؤدب | المعنى |
|-------|-------------|-------------|--------|
| كبير | 크다 | 커요 | كبير |
| صغير | 작다 | 작아요 | صغير |
| حلو | 예쁘다 | 예뻐요 | حلو/جميل |
| لذيذ | 맛있다 | 맛있어요 | لذيذ |
| ممتع | 재미있다 | 재미있어요 | ممتع |

**مقارنة عربية**: في العربية الصفة كلمة منفصلة (الولد **الكبير**). في الكورية الصفة ممكن تكون فعل الجملة: 이 집이 커요 (هذا البيت كبير — حرفياً: هذا البيت "يكبر").

