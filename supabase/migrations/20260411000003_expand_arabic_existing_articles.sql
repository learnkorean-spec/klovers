## نظام المقاطع الكورية

الحروف الكورية مش بتتكتب في سطر زي الإنجليزي — بتتجمع في **مقاطع** (syllable blocks):

**النمط 1:** ساكن + متحرك
- ㅂ + ㅏ = 바 (ba)

**النمط 2:** ساكن + متحرك + ساكن نهائي
- ㅂ + ㅏ + ㅂ = 밥 (bap = أرز)

**النمط 3:** ساكن + متحرك مركب
- ㅎ + ㅘ = 화 (hwa)

