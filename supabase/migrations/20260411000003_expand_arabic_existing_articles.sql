## المشاعر والردود

**7. 진짜요? (jinjjayo?)** — *بجد؟ فعلاً؟*
بتتقال بعيون واسعة في كل دراما. هتسمعها 10 مرات على الأقل في كل حلقة.

**8. 대박 (daebak)** — *يا سلام! / مش معقول!*
تعبير عن المفاجأة أو الإعجاب. معناها الأصلي "الجائزة الكبرى."

**9. 아이고 (aigo)** — *يا لهوي! / يا إلهي!*
المعادل الكوري لـ "يا لهوي" أو "يا نهار أبيض" بالمصري. بتستخدمها الجدات الكوريات كتير. تعبّر عن مفاجأة، إحباط، تعب، أو حنان.

**10. 어떡해 (eotteokae)** — *أعمل إيه؟ / يا خبر!*
عبارة الذعر الكورية. بتتقال مع مسك الوش أو المشي ذهاب وإياب.

**11. 설레다 (seolleda)** — *قلبي بيدق / حاسس بفراشات*
الكلمة الكورية للإحساس اللي بتحسه في بداية قصة حب. مفيش مقابل دقيق بالعربي.

**12. 미치겠다 (michigessda)** — *هتجنن!*
بتستخدم لما تكون محبط أو مجنون من حد (حب أو غضب).

