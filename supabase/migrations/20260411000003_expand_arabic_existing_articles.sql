## تحيات والحياة اليومية

**1. 안녕하세요 (annyeonghaseyo)** — *مرحبا*
مستوى الرسمية: مؤدب. تستخدمها مع أي حد.
في الدراما: أول جملة في أي مشهد تعارف.

**2. 안녕 (annyeong)** — *أهلاً / باي*
مستوى الرسمية: غير رسمي. بس مع الأصدقاء المقربين.

**3. 잘 지냈어요? (jal jinaesseoyo?)** — *إزيك؟ / عامل إيه؟*
حرفياً: "كنت كويس؟" تستخدمها لما تقابل حد من فترة طويلة.

**4. 오랜만이에요 (oraenmanieyo)** — *بقالنا كتير!*
في الدراما: الحلقة الأولى لما أصدقاء الطفولة يتقابلوا كبار.

**5. 밥 먹었어요? (bap meogeosseoyo?)** — *أكلت؟*
دي مش سؤال عن الأكل فعلاً. الكوريين بيقولوها زي ما المصريين بيقولوا "اتغديت؟" — يعني "أنا مهتم بيك."

**6. 다녀오겠습니다 (danyeoogesseumnida)** — *أنا ماشي وراجع*
بتتقال لما حد يطلع من البيت. الرد: 다녀와 (danyeowa) — "ارجع بالسلامة."

