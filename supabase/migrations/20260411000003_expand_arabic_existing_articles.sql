## الأفعال الكورية: الأساسيات

### الشكل القاموسي (Dictionary Form)

كل الأفعال الكورية بتنتهي بـ -다 (da):
- 먹다 (meokda) = يأكل
- 가다 (gada) = يذهب
- 마시다 (masida) = يشرب
- 공부하다 (gongbuhada) = يدرس

### تصريف الأفعال: المؤدب (해요체)

عشان تستخدم الفعل في المحادثة، غيّر النهاية:

| الفعل | الجذر | التصريف المؤدب | المعنى |
|-------|-------|---------------|--------|
| 먹다 | 먹 | 먹어요 | بآكل |
| 가다 | 가 | 가요 | بروح |
| 마시다 | 마시 | 마셔요 | بشرب |
| 공부하다 | 공부하 | 공부해요 | بدرس |
| 보다 | 보 | 봐요 | بشوف |
| 듣다 | 듣 | 들어요 | بسمع |

**القاعدة**:
- لو آخر حرف متحرك في الجذر ㅏ أو ㅗ → أضف 아요
- غير كده → أضف 어요
- الأفعال اللي بتنتهي بـ 하다 → تتحول لـ 해요

