## القواعد الأساسية: ترتيب الجملة

أكبر فرق بين الكورية والعربية هو ترتيب الجملة:

| اللغة | الترتيب | مثال |
|-------|---------|------|
| العربية | فعل + فاعل + مفعول | آكل التفاحة |
| الكورية | فاعل + مفعول + فعل | 나는 사과를 먹어요 (أنا التفاحة آكل) |

### الجسيمات (Particles) — أهم مفهوم في الكورية

الكورية تستخدم "جسيمات" بعد الكلمات لتحديد دورها في الجملة:

| الجسيم | الوظيفة | مثال |
|--------|---------|------|
| 은/는 | موضوع الجملة | 저**는** 학생이에요 (أنا طالب) |
| 이/가 | فاعل | 비**가** 와요 (المطر يأتي) |
| 을/를 | مفعول به | 밥**을** 먹어요 (آكل الأرز) |
| 에 | مكان/زمان | 학교**에** 가요 (أذهب إلى المدرسة) |
| 에서 | مكان الفعل | 집**에서** 공부해요 (أدرس في البيت) |

> **نصيحة**: الجسيمات مش موجودة في العربية، لكن فكر فيها كحروف الجر المتصلة بالكلمة.

